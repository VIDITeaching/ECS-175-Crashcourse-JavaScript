'use strict'

/* 
Literals are values that we can represent in JS.
The main ones are string, number, boolean, list and object.
*/


// String
"I am a string";
'I am also a string';

// Number
10;
2.6;
10e-3;
-4;
0x5F;

// Boolean
true;
false;

// List
[];
[1, 2, 3, 4, 5];
['a', 'b', 'cat'];
[1, 'b', 3, 'd', true];

// Object
{};
{
    name: "David",
    role: "TA",
    year: 2
};